package com.java.codes;

public class Relational {
    public static void main(String[] args) {
        int a = 5;
        int b = 13;

        //Comparison Operators
        System.out.println("a>b : "+(a>b));
        System.out.println("a<b : "+(a<b));
        System.out.println("a>=b : "+(a>=b));
        System.out.println("a<=b : "+(a<=b));

        // Equality Operators
        System.out.println("a==b : "+(a==b));
        System.out.println("a!=b : "+(a!=b));

    }
}
